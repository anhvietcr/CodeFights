int largestNumber(int n) {
    switch(n){
        case 1: return 9; break;
        case 2: return 99; break;
        case 3: return 999; break;    
        case 4: return 9999; break;
        case 5: return 99999; break;
        case 6: return 999999; break;
        case 7: return 9999999; break;
        default: return 0; break;
    }
}
